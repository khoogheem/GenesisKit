/**
	Extensions of GKColor for Colors of Companies
*/
public extension GKColor {

	//MARK: A
	
	/**
	Returns a color object whose RGBA values are (r 0.514 g 0.722 b 0.102 a 1.0) which represents Acer
	
	:returns: Returns The Color Object
	*/
